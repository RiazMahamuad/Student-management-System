    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int chackid=0;
        String id=jTextField1.getText();
        try
        {
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from studentms where id= '"+id+"'");
            while(rs.next())
            {
                chackid=1;
                jTextField1.setEditable(false);
                jTextField2.setText(rs.getString(2));
                jTextField9.setText(rs.getString(3));
                jTextField14.setText(rs.getString(4));             
               jComboBox1.setSelectedItem(rs.getString(5));
               jComboBox2.setSelectedItem(rs.getString(6));
                jTextField15.setText(rs.getString(7));
                jTextField17.setText(rs.getString(8));             
                jComboBox3.setSelectedItem(rs.getString(9));
                jTextField11.setText(rs.getString(10));
                jComboBox4.setSelectedItem(rs.getString(11));
                
            }
            if(chackid==0)
            {
                JOptionPane.showMessageDialog(null,"Student ID does not match");
            }
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      String id=jTextField1.getText();
      String name=jTextField2.getText();
      String email=jTextField9.getText();
      String mobile=jTextField14.getText();
      String deferment=jComboBox1.getSelectedItem().toString();
      String sub=jComboBox2.getSelectedItem().toString();
      String fname=jTextField15.getText();
      String mname=jTextField17.getText();
      String shift=jComboBox3.getSelectedItem().toString();
      String age=jTextField11.getText();
      String gender=jComboBox4.getSelectedItem().toString();
      try
      {
             Connection con=ConnectionProvider.getCon();
             PreparedStatement pst=con.prepareStatement("update studentms set Name=?,Mobile=?,Email=?,Deferment=?,Subject=?,FatherName=?,MotherName=?,Shift=?,Age=?,Gender=? where id=?");
             
            pst.setString(1, name);
            pst.setString(2, email);
            pst.setString(3, mobile);
            pst.setString(4, deferment);
            pst.setString(5, sub);
            pst.setString(6, fname);
            pst.setString(7, mname);
            pst.setString(8, shift);
            pst.setString(9, age);
            pst.setString(10, gender);
             pst.setString(11, id);
            pst.executeUpdate();
             JOptionPane.showMessageDialog(this, "Succesfully Update Data...");
             setVisible(false);
             new Update().setVisible(true);
      }
      catch(Exception e)
      {
          JOptionPane.showMessageDialog(null, e);
      }
      
    }//GEN-LAST:event_jButton3ActionPerformed

